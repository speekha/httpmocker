package fr.speekha.httpmocker.ktor.io

import kotlinx.coroutines.CoroutineDispatcher
import kotlinx.coroutines.Runnable
import platform.darwin.dispatch_async
import platform.darwin.dispatch_get_main_queue
import platform.darwin.dispatch_io_t
import platform.darwin.dispatch_queue_t
import kotlin.coroutines.CoroutineContext

private class NsQueueDispatcher(private val dispatchQueue: dispatch_queue_t) :
    CoroutineDispatcher() {
    override fun dispatch(context: CoroutineContext, block: Runnable) {
        dispatch_async(dispatchQueue) {
            block.run()
        }
    }
}

internal actual val dispatcherIO: CoroutineDispatcher = NsQueueDispatcher(dispatch_io_t())
